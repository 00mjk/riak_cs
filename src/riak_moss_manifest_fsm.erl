-module(riak_moss_manifest_fsm).

-behaviour(gen_fsm).

%% API
-export([start_link/2]).

%% gen_fsm callbacks
-export([init/1,

         %% async
         prepare/2,
         waiting_command/2,
         waiting_update_command/2,

         %% sync
         waiting_command/3,

         %% rest
         handle_event/3,
         handle_sync_event/4,
         handle_info/3,
         terminate/3,
         code_change/4]).

-define(SERVER, ?MODULE).

-record(state, {}).

%%%===================================================================
%%% API
%%%===================================================================

%%--------------------------------------------------------------------
%% @doc
%% Creates a gen_fsm process which calls Module:init/1 to
%% initialize. To ensure a synchronized start-up procedure, this
%% function does not return until Module:init/1 has returned.
%%
%% @spec start_link() -> {ok, Pid} | ignore | {error, Error}
%% @end
%%--------------------------------------------------------------------
start_link(Bucket, Key) ->
        gen_fsm:start_link({local, ?SERVER}, ?MODULE, [Bucket, Key], []).

%%%===================================================================
%%% gen_fsm callbacks
%%%===================================================================

%%--------------------------------------------------------------------
%% @private
%% @doc
%% Whenever a gen_fsm is started using gen_fsm:start/[3,4] or
%% gen_fsm:start_link/[3,4], this function is called by the new
%% process to initialize.
%%
%% @spec init(Args) -> {ok, StateName, State} |
%%                     {ok, StateName, State, Timeout} |
%%                     ignore |
%%                     {stop, StopReason}
%% @end
%%--------------------------------------------------------------------
init([_Bucket, _Key]) ->
    %% purposely have the timeout happen
    %% so that we get called in the prepare
    %% state
    {ok, prepare, #state{}, 0}.

%%--------------------------------------------------------------------
%% @private
%% @doc
%% There should be one instance of this function for each possible
%% state name. Whenever a gen_fsm receives an event sent using
%% gen_fsm:send_event/2, the instance of this function with the same
%% name as the current state name StateName is called to handle
%% the event. It is also called if a timeout occurs.
%%
%% @spec state_name(Event, State) ->
%%                   {next_state, NextStateName, NextState} |
%%                   {next_state, NextStateName, NextState, Timeout} |
%%                   {stop, Reason, NewState}
%% @end
%%--------------------------------------------------------------------
prepare(timeout, State) ->
    %% TODO:
    %% add in code that grabs
    %% a riakc_pb_socket pid
    %% and stash it in our state

    %% also not sure if this is the place
    %% that we should go retrieve the
    %% value from riak or do it when
    %% we get a request to retrieve
    %% the active manifest
    {next_state, waiting_command, State}.

%% This clause is for adding a new
%% manifest that doesn't exist yet.
%% Once it has been called _once_
%% with a particular UUID, update_manifest
%% should be used from then on out.
waiting_command({add_new_manifest, _Manifest}, State) ->
    {next_state, waiting_update_command, State}.

waiting_update_command({delete_manifest, _Manifest}, State) ->
    {stop, normal, State};
waiting_update_command({update_manifest, _Manifest}, State) ->
    {next_state, waiting_update_command, State};
waiting_update_command(stop, State) ->
    {stop, normal, State}.


%%--------------------------------------------------------------------
%% @private
